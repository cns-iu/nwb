package edu.iu.sci2.database.isi.load.utilities.parser.exception;

public class ReferenceParsingException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public ReferenceParsingException() {
		super();
	}

	public ReferenceParsingException(String arg0) {
		super(arg0);
	}

	public ReferenceParsingException(Throwable arg0) {
		super(arg0);
	}

	public ReferenceParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
