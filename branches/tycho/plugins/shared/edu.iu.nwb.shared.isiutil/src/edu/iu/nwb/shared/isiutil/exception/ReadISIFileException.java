package edu.iu.nwb.shared.isiutil.exception;

public class ReadISIFileException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public ReadISIFileException() {
		super();
	}

	public ReadISIFileException(String arg0) {
		super(arg0);
	}

	public ReadISIFileException(Throwable arg0) {
		super(arg0);
	}

	public ReadISIFileException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
