package edu.iu.nwb.analysis.blondelcommunitydetection;

public class TreeFileParsingException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public TreeFileParsingException() {
		super();
	}

	public TreeFileParsingException(String arg0) {
		super(arg0);
	}

	public TreeFileParsingException(Throwable arg0) {
		super(arg0);
	}

	public TreeFileParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}