package edu.iu.nwb.util.nwbfile;

public class NWBMetadataParsingException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public NWBMetadataParsingException() {
		super();
	}

	public NWBMetadataParsingException(String arg0) {
		super(arg0);
	}

	public NWBMetadataParsingException(Throwable arg0) {
		super(arg0);
	}

	public NWBMetadataParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}