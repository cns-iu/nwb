package edu.iu.sci2.database.isi.load.utilities.exception;

public class ModelCreationException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public ModelCreationException() {
		super();
	}

	public ModelCreationException(String arg0) {
		super(arg0);
	}

	public ModelCreationException(Throwable arg0) {
		super(arg0);
	}

	public ModelCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
