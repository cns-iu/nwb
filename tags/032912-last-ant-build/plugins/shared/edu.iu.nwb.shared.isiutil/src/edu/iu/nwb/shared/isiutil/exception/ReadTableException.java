package edu.iu.nwb.shared.isiutil.exception;

public class ReadTableException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public ReadTableException() {
		super();
	}

	public ReadTableException(String arg0) {
		super(arg0);
	}

	public ReadTableException(Throwable arg0) {
		super(arg0);
	}

	public ReadTableException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
