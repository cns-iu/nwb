package edu.iu.nwb.shared.isiutil.exception;

public class CitationExtractionPreparationException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public CitationExtractionPreparationException() {
		super();
	}

	public CitationExtractionPreparationException(String arg0) {
		super(arg0);
	}

	public CitationExtractionPreparationException(Throwable arg0) {
		super(arg0);
	}

	public CitationExtractionPreparationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
