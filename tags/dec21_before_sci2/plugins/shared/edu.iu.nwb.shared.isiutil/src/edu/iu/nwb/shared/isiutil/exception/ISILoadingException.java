package edu.iu.nwb.shared.isiutil.exception;

public class ISILoadingException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public ISILoadingException() {
		super();
	}

	public ISILoadingException(String arg0) {
		super(arg0);
	}

	public ISILoadingException(Throwable arg0) {
		super(arg0);
	}

	public ISILoadingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
