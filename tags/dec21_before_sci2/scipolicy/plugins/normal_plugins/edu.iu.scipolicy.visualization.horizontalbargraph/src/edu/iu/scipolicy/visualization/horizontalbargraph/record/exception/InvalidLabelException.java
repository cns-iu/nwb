package edu.iu.scipolicy.visualization.horizontalbargraph.record.exception;

public class InvalidLabelException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public InvalidLabelException() {
		super();
	}

	public InvalidLabelException(String arg0) {
		super(arg0);
	}

	public InvalidLabelException(Throwable arg0) {
		super(arg0);
	}

	public InvalidLabelException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}