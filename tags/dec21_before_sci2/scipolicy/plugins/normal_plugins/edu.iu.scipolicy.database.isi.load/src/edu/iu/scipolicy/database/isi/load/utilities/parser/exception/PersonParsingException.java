package edu.iu.scipolicy.database.isi.load.utilities.parser.exception;

public class PersonParsingException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public PersonParsingException() {
		super();
	}

	public PersonParsingException(String arg0) {
		super(arg0);
	}

	public PersonParsingException(Throwable arg0) {
		super(arg0);
	}

	public PersonParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
