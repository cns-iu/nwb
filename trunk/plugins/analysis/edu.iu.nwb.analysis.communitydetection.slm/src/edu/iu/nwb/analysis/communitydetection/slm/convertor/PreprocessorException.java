package edu.iu.nwb.analysis.communitydetection.slm.convertor;

public class PreprocessorException extends RuntimeException {
	private static final long serialVersionUID = 1L;
	
	public PreprocessorException() {
		super();
	}

	public PreprocessorException(String arg0) {
		super(arg0);
	}

	public PreprocessorException(Throwable arg0) {
		super(arg0);
	}

	public PreprocessorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}