package edu.iu.epic.visualization.linegraph.utilities;

public class StencilRunnerCreationException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public StencilRunnerCreationException() {
		super();
	}

	public StencilRunnerCreationException(String arg0) {
		super(arg0);
	}

	public StencilRunnerCreationException(Throwable arg0) {
		super(arg0);
	}

	public StencilRunnerCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}