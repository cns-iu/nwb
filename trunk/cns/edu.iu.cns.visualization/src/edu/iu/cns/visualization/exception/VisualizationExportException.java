package edu.iu.cns.visualization.exception;

public class VisualizationExportException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public VisualizationExportException() {
		super();
	}

	public VisualizationExportException(String arg0) {
		super(arg0);
	}

	public VisualizationExportException(Throwable arg0) {
		super(arg0);
	}

	public VisualizationExportException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}