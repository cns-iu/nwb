package edu.iu.scipolicy.loader.nsf.db.exception;

public class NSFReadingException extends Exception {
	
	private static final long serialVersionUID = 1L;
	
	public NSFReadingException() {
		super();
	}

	public NSFReadingException(String arg0) {
		super(arg0);
	}

	public NSFReadingException(Throwable arg0) {
		super(arg0);
	}

	public NSFReadingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
