package edu.iu.scipolicy.database.nsf.loader.exception;

public class NSFDatabaseCreationException extends Exception {
	
	private static final long serialVersionUID = 1L;
	
	public NSFDatabaseCreationException() {
		super();
	}

	public NSFDatabaseCreationException(String arg0) {
		super(arg0);
	}

	public NSFDatabaseCreationException(Throwable arg0) {
		super(arg0);
	}

	public NSFDatabaseCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
