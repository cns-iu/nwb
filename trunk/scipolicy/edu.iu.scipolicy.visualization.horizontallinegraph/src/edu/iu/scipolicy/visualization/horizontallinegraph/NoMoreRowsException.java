package edu.iu.scipolicy.visualization.horizontallinegraph;

public class NoMoreRowsException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public NoMoreRowsException() {
		super();
	}

	public NoMoreRowsException(String arg0) {
		super(arg0);
	}

	public NoMoreRowsException(Throwable arg0) {
		super(arg0);
	}

	public NoMoreRowsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}