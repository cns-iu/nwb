package edu.iu.scipolicy.visualization.horizontallinegraph;

public class GrantCreationException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public GrantCreationException() {
		super();
	}

	public GrantCreationException(String arg0) {
		super(arg0);
	}

	public GrantCreationException(Throwable arg0) {
		super(arg0);
	}

	public GrantCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}