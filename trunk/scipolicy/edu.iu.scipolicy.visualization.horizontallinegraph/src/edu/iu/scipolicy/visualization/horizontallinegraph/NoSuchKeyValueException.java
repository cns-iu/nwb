package edu.iu.scipolicy.visualization.horizontallinegraph;

public class NoSuchKeyValueException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public NoSuchKeyValueException() {
		super();
	}

	public NoSuchKeyValueException(String arg0) {
		super(arg0);
	}

	public NoSuchKeyValueException(Throwable arg0) {
		super(arg0);
	}

	public NoSuchKeyValueException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}