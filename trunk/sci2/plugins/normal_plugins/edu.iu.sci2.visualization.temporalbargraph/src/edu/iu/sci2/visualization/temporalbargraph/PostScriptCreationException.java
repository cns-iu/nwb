package edu.iu.sci2.visualization.temporalbargraph;

public class PostScriptCreationException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public PostScriptCreationException() {
		super();
	}

	public PostScriptCreationException(String arg0) {
		super(arg0);
	}

	public PostScriptCreationException(Throwable arg0) {
		super(arg0);
	}

	public PostScriptCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}