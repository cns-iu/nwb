package edu.iu.sci2.visualization.horizontalbargraph.record.exception;

public class BadDatasetException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public BadDatasetException() {
		super();
	}

	public BadDatasetException(String arg0) {
		super(arg0);
	}

	public BadDatasetException(Throwable arg0) {
		super(arg0);
	}

	public BadDatasetException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}